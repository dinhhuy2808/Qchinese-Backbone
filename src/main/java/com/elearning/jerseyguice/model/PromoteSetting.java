package com.elearning.jerseyguice.model;

public class PromoteSetting {
	private Integer hsk;
	private Integer wordAmount;
	private Integer avgExerciseScore;
	private Integer promoteChain;
	private Integer scorePerTest;
	private Integer targetRankingId;
	private Integer factor;
	private Integer tests;
	public Integer getHsk() {
		return hsk;
	}
	public void setHsk(Integer hsk) {
		this.hsk = hsk;
	}
	public Integer getWordAmount() {
		return wordAmount;
	}
	public void setWordAmount(Integer wordAmount) {
		this.wordAmount = wordAmount;
	}
	public Integer getAvgExerciseScore() {
		return avgExerciseScore;
	}
	public void setAvgExerciseScore(Integer avgExerciseScore) {
		this.avgExerciseScore = avgExerciseScore;
	}
	public Integer getPromoteChain() {
		return promoteChain;
	}
	public void setPromoteChain(Integer promoteChain) {
		this.promoteChain = promoteChain;
	}
	public Integer getScorePerTest() {
		return scorePerTest;
	}
	public void setScorePerTest(Integer scorePerTest) {
		this.scorePerTest = scorePerTest;
	}
	public Integer getTargetRankingId() {
		return targetRankingId;
	}
	public void setTargetRankingId(Integer targetRankingId) {
		this.targetRankingId = targetRankingId;
	}
	public Integer getFactor() {
		return factor;
	}
	public void setFactor(Integer factor) {
		this.factor = factor;
	}
	public Integer getTests() {
		return tests;
	}
	public void setTests(Integer tests) {
		this.tests = tests;
	}
}
