package com.elearning.model;

import java.util.List;

public class CourseHolder {
	private Integer hsk;
	private List<Lesson> lessions;
	public Integer getHsk() {
		return hsk;
	}
	public void setHsk(Integer hsk) {
		this.hsk = hsk;
	}
	public List<Lesson> getLessions() {
		return lessions;
	}
	public void setLessions(List<Lesson> lessions) {
		this.lessions = lessions;
	}
	
	
}
